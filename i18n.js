// i18n.js — bilingual UI strings (English / Swedish). Loaded after lib.js,
// before every page script. Swedish is the target buyer's language (Fortnox
// users are Swedish SMEs); English stays the default so existing dev/test
// workflows and screenshots don't change underfoot — switch explicitly via
// the sidebar toggle or ?lang=sv for testing, same convention as ?theme=light.
//
// STRINGS values are either a plain string, or a function for anything with
// an interpolated value (amounts, names, counts) — keeps every translated
// sentence explicit and reviewable, instead of assembling sentences from
// smaller fragments (which reads wrong in Swedish word order half the time).
//
// Numbers/dates already render via toLocaleString("sv-SE", ...) regardless
// of UI language (that's a currency-formatting choice, not a language one) —
// t() only ever touches words.

function getLang() {
  return localStorage.getItem("almgren-budget-lang")
    || new URLSearchParams(location.search).get("lang")
    || "en";
}

function setLang(lang) {
  localStorage.setItem("almgren-budget-lang", lang);
  location.reload();
}

function t(key, ...args) {
  const lang = getLang();
  const entry = (STRINGS[lang] && STRINGS[lang][key]) ?? STRINGS.en[key];
  if (entry === undefined) return key; // missing translation is visible, not a crash
  return typeof entry === "function" ? entry(...args) : entry;
}

// Static HTML (the markup baked into each page, never re-rendered by JS)
// carries data-i18n="key" (plain text) or data-i18n-html="key" (has nested
// markup, e.g. a <span> sub-label) instead of a t() call — there's no JS
// render pass to hook into for that markup. Runs once, immediately: by the
// time this script tag executes, everything above it in the HTML source has
// already been parsed into the DOM (script tags sit at the bottom of every
// page here), so there's nothing to wait for.
function translateStaticHtml() {
  document.querySelectorAll("[data-i18n]").forEach((el) => { el.textContent = t(el.dataset.i18n); });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => { el.innerHTML = t(el.dataset.i18nHtml); });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => { el.placeholder = t(el.dataset.i18nPlaceholder); });
}

